<p>Si haz sufrido o padecés alguna lesión, enfermedad o condición por la que necesités atención médica&nbsp;o cuidado, antes de continuar con los preparativos para el viaje debés comprar una <b>Asistencia al viajero especial con cobertura de enfermedades crónicas o preexistencias.</b></p>
